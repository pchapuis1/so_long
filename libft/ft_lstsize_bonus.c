#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*temp;

	if (!lst)
		return (0);
	i = 0;
	temp = lst;
	while (lst)
	{
		lst = lst->next;
		i ++;
	}
	return (i);
}
